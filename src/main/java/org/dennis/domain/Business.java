package org.dennis.domain;

import java.util.Date;

public class Business {
    private Integer busId;

    private String busName;

    private String busPass;

    private String busAvatar;

    private Integer busScore;

    private String busSend;

    private String busPhone;

    private String busAddr;

    private Integer busReco;

    private Boolean busStatus;

    private Date addTime;

    private Integer busAct;

    public Integer getBusId() {
        return busId;
    }

    public void setBusId(Integer busId) {
        this.busId = busId;
    }

    public String getBusName() {
        return busName;
    }

    public void setBusName(String busName) {
        this.busName = busName;
    }

    public String getBusPass() {
        return busPass;
    }

    public void setBusPass(String busPass) {
        this.busPass = busPass;
    }

    public String getBusAvatar() {
        return busAvatar;
    }

    public void setBusAvatar(String busAvatar) {
        this.busAvatar = busAvatar;
    }

    public Integer getBusScore() {
        return busScore;
    }

    public void setBusScore(Integer busScore) {
        this.busScore = busScore;
    }

    public String getBusSend() {
        return busSend;
    }

    public void setBusSend(String busSend) {
        this.busSend = busSend;
    }

    public String getBusPhone() {
        return busPhone;
    }

    public void setBusPhone(String busPhone) {
        this.busPhone = busPhone;
    }

    public String getBusAddr() {
        return busAddr;
    }

    public void setBusAddr(String busAddr) {
        this.busAddr = busAddr;
    }

    public Integer getBusReco() {
        return busReco;
    }

    public void setBusReco(Integer busReco) {
        this.busReco = busReco;
    }

    public Boolean getBusStatus() {
        return busStatus;
    }

    public void setBusStatus(Boolean busStatus) {
        this.busStatus = busStatus;
    }

    public Date getAddTime() {
        return addTime;
    }

    public void setAddTime(Date addTime) {
        this.addTime = addTime;
    }

    public Integer getBusAct() {
        return busAct;
    }

    public void setBusAct(Integer busAct) {
        this.busAct = busAct;
    }
}